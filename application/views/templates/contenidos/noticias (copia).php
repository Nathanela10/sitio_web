                </p>
                <B> ¿A qué se deben las intensas lluvias los últimos días? </B>

                <p>
                    En la región andina y por ende en el Valle de Aburrá el régimen de precipitación está caracterizado por
                    dos temporadas secas (con valores de precipitación bajos) y dos temporadas de lluvias, condicionadas por
                    el paso de la zona de convergencia intertropical (ZCIT) sobre el territorio. Las temporadas con menos
                    lluvias ocurren durante el trimestre (Diciembre - Enero - Febrero) y a mediados del año (Junio - Julio -
                    Agosto) y las de lluvia durante los meses intermedios, es decir, Marzo - Abril -Mayo y Septiembre -
                    Octubre - Noviembre.
                </p>
