    <p style="text-align: justify;">
        La AGU (American Geophysical Union – Unión Americana de Geofísica) es una asociación internacional sin ánimo de
        lucro creada en 1919 que agrupa una comunidad de 60.000 científicos de ciencias de la Tierra y el Espacio,
        originarios de 137 países. Se congregan para trabajar de forma colaborativa para favorecer los avances en
        investigación, la comunicación de la ciencia y su poder de garantizar un futuro sostenible. Su misión es
        promover el conocimiento de la Tierra y el Espacio para el beneficio de la humanidad.
        <br>
        AGU Fall metting, Las reuniones anuales son un momento para intercambiar y socializar experiencias, conocer los
        últimos avances y descubrimientos. Convirtiéndose así en la plataforma para impulsar el conocimiento, establecer
        redes y estimular nuevos retos en investigación. Este año la reunión tuvo lugar del 11 al 15 de diciembre en la
        ciudad de New Orleans en Estados Unidos.
