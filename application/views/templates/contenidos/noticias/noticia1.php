    <p style="text-align: justify;">
        <B> ¿A qué se deben las intensas lluvias los últimos días? </B>
        <br> <br>
        En la región andina y por ende en el Valle de Aburrá el régimen de precipitación está caracterizado por dos
        temporadas secas (con valores de precipitación bajos) y dos temporadas de lluvias, condicionadas por el paso de
        la zona de convergencia intertropical (ZCIT) sobre el territorio. Las temporadas con menos lluvias ocurren
        durante el trimestre (Diciembre - Enero - Febrero) y a mediados del año (Junio - Julio - Agosto) y las de lluvia
        durante los meses intermedios, es decir, Marzo - Abril -Mayo y Septiembre - Octubre - Noviembre.
        <br> <br>
        Octubre es uno de los meses más lluviosos del año con acumulados alrededor de 100 - 200 mm en Medellín. Está
        caracterizado por lluvias convectivas de gran intensidad y corta duración, que ocurren principalmente en horas
        de la tarde y lluvias estratiformes en las horas de la madrugada. De acuerdo con esto, los eventos de
        precipitación que se han presentado en las últimas semanas hacen parte de la segunda temporada de lluvias de la
        región y si bien han sido muy intensos obedecen al paso de la ZCIT sobre nuestro territorio.
        <br> <br>
        Otra característica importante de las temporadas de lluvias es la ocurrencia de tormentas eléctricas debido a
        que por nuestra ubicación en el trópico y la compleja topografía, nubes de gran desarrollo vertical
        (cumulunimbus) con procesos de electrificación más activos son frecuentes . Estas tormentas son más comunes en
        esta época del año en comparación con los trimestres menos lluviosos. Las tormentas eléctricas están asociadas
        en general a lluvias de alta intensidad, típicas de las temporadas de lluvias de por lo que hay una concordancia
        con el ciclo anual de precipitación y la ocurrencia de tormentas eléctricas.
        <br><br> <br>
        <B> ¿Se pronostica una temporada invernal?</B>
        <br> <br>
        En nuestra región no debemos hablar de temporada invernal, sino de temporada de lluvias, pues por nuestra
        localización geográfica (trópico) no tenemos estaciones como sí ocurre en latitudes medias.
        <br> <br>
        Además como se mencionó anteriormente, son condiciones típicas de la época o climatológicas, es decir, se
        repiten cada año; entonces siempre vamos a tener durante el trimestre Septiembre Noviembre un incremento en las
        lluvias (460 mm en promedio, estación EPM Edificio Miguel de Aguinaga) respecto al trimestre Junio-Agosto (330
        mm en promedio, estación EPM Edificio Miguel de Aguinaga). De lo que sí podemos hablar es sobre qué tan fuerte
        será la temporada de lluvias en términos de acumulados, es decir, si los acumulados de lluvia serán cercanos a
        los valores promedio históricos o serán inferiores o más altos. Esto debido a que nuestro clima y en particular
        el régimen de precipitación está condicionada por fenómenos climáticos de escala global como el fenómeno de El
        Niño – Oscilación del Sur (ENSO) que en su fase cálida generalmente produce una disminución en los acumulados de
        lluvia en la región andina. En este momento las condiciones del Pacífico tropical son indicativas de fase cálida
        y según los pronósticos del ENSO, existe un 70% de probabilidad de que se desarrolle un fenómeno de El Niño a
        finales del año en curso, con lo cual esperaríamos una temporada de lluvias un poco más seca que lo esperado (en
        acumulado total), pero con eventos intensos como los que hemos tenido.
        <br> <br> <br>
        <b>¿Cuánto dura la temporada de lluvias? </b>
        <br> <br>
        Típicamente la temporada de lluvias va entre septiembre y noviembre, siendo octubre el mes con mayores
        acumulados y eventos más intensos. No podemos decir exactamente fechas entre las cuales tenga lugar pero hacia
        mediados-finales de noviembre empezamos a notar la transición hacia un diciembre caracterizado por bajos
        acumulados de precipitación, altas temperaturas y días más despejados.
