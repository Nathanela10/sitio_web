    <br>
    En conclusión, dado que las condiciones meteorológicas propias de la temporada de transición dificultan la
    dispersión de contaminantes en la atmósfera, y esto se ve claramente en los registros de las estaciones de la Red de
    Monitoreo de Calidad del Aire, con el aumento de la concentración de PM 2.5, debemos estar preparados
    <br><br>
    3. ¿Cómo se espera que se comporten estas condiciones durante el primer semestre del año, y cómo se prepara el Área
    Metropolitana frente a una posible contingencia? La temporada de transición traerá nuevamente las condiciones
    meteorológicas que dan origen a los episodios críticos de calidad del aire. Se espera que esto ocurra entre finales
    del mes de febrero y la primera mitad del mes de marzo, y es por esto que el Área Metropolitana del Valle de Aburrá
    preparó el
    <a target="_blank" href="http://www.metropol.gov.co/Documents/AcuerdoN15.PDF" style="color: blue;">Plan Operacional
        para enfrentar Episodios Críticos de Contaminación Atmosférica </a>.
    <br>
    Dentro de este Plan, el SIATA, tanto como operador de la Red de Monitoreo de Calidad del Aire del Valle de Aburrá,
    así como proyecto encargado de hacer seguimiento de las condiciones meteorológicas de la región, tiene la
    responsabilidad de alertar de manera oportuna a los integrantes del Grupo de Gestión de Episodios Críticos de
    Contaminación Atmosférica -GECA-, sobre cualquier irregularidad que se observe o se pronostique, para tomar las
    medidas necesarias.
    <br><br>
