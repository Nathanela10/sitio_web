    2. Las condiciones meteorológicas desfavorables para la dispersión de material particulado que se extendieron
    durante más de un mes, sumadas a la continua emisión de contaminantes por parte de fuentes fijas y móviles, dieron
    como resultado un aumento significativo de PM 2.5 en los registros y, como consecuencia, un
    <a target="_blank" href="http://www.metropol.gov.co/CalidadAire/Paginas/ica.aspx" style="color: blue;">Índice de
        Calidad del Aire </a> dañino a la salud en todas las
    <a target="_blank" href="https://www.youtube.com/watch?v=v5H4dQ9ieJ0" style="color: blue;"> estaciones de
        representatividad poblacional </a> que están instaladas a lo largo del Valle de Aburrá.
    <br>
    Como se observa en la siguiente gráfica, a partir de febrero, en la mayoría de las estaciones, se empiezan a
    registrar ICA dañinos para grupos sensibles (color naranja) y la situación se agudiza a partir de marzo, cuando el
    ICA en todas las estaciones es dañino a la salud (color rojo).

