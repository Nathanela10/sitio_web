            El ruido es un contaminante atmosférico de interés público relevante, dadas las afectaciones que genera en la calidad de vida de los y 
            las habitantes del Valle de Aburrá. 
            </p>
            <p>
            Una de las definiciones más aceptadas sobre ruido es todo aquel sonido no deseado y que puede calificarse como algo molesto,
             inoportuno o desagradable. En particular, el ruido ambiental es un contaminante físico que se puede definir como aquel tipo de sonido que 
             se encuentra a nuestro alrededor y que en el caso de las ciudades, está asociado principalmente a los medios de transporte, la industria, 
             el comercio y el ocio de la población.  
            </p>
            <p>
            Para monitorear este contaminante se utilizan sonómetros, dispositivos que permiten conocer el nivel de presión sonora en decibeles, y a 
            su vez, simular la forma como el oído humano percibe el sonido (a través de un filtro llamado 'ponderación A').   
            </p>
            <p>
            Para tener un contexto de los niveles de ruido que escuchamos en nuestra vida cotidiana podemos decir que, en un bosque silencioso se podrían 
            tener valores de 30 dBA, una conversación normal son 60 dBA aprox. o una calle ruidosa ronda los 80 dBA. La Organización Mundial de la 
            Salud -OMS, recomienda 55 dB como el nivel de ruido ambiental recomendable para las ciudades.  
            </p>
            <p>
            Desde SIATA, proyecto del Área Metropolitana, contamos con una Red de Monitoreo de Ruido Ambiental que proporciona información técnica 
            necesaria para la formulación y desarrollo de políticas públicas, y estrategias para la gestión del ruido y el mejoramiento de la calidad 
            acústica de los municipios. Esta red está conformada por ocho estaciones ubicadas en los municipios de Medellín, Itagüí, Girardota y Sabaneta,
             y  se encuentran instaladas en zonas con influencia del tráfico vehicular y de locales comerciales como restaurantes, bares y discotecas.  
            </p>
            <p>
            El objetivo de la Red de Monitoreo de Ruido Ambiental es proporcionar la información técnica necesaria para la formulación y desarrollo 
            de políticas públicas y estrategias para la gestión del ruido y el mejoramiento de la calidad acústica de los municipios. A través de los
             datos generados se logra elaborar mapas de ruido ambiental, representaciones gráficas de los niveles de ruido en una zona específica para
              definir el grado de ruido al que está expuesto un determinado grupo de personas. Además, son relevantes para el ordenamiento del territorio,
               la definición de políticas y normas, y el desarrollo de estrategias de prevención, mitigación, control y seguimiento. 
            </p>
