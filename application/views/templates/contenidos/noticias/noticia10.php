    <p style="text-align: justify;">
        Aprender a vivir en armonía con el planeta que habitamos es fundamental para garantizar nuestra supervivencia y
        la de nuestro territorio; la educación se convierte en un camino posible para lograrlo y SIATA – Sistema de
        Alerta Temprana de Medellín y el Valle de Áburra- como proyecto de gestión de riesgos entiende la importancia de
        conocer y comprender el lugar en el que vivimos, es por esto que creemos en los procesos de educación ambiental
        como una posibilidad de habitar nuestro territorio de una manera responsable

        <br>
        La educación ambiental (EA) debe ser una acción permanente por la cual las personas toman conciencia de su
        realidad, del tipo de relaciones que establecen con los demás seres vivo y de los problemas derivados de dichas
        relaciones. La EA y la gestión de riesgos son acciones que se deben pensar de manera conjunta, porque es
        reconociendo nuestro contexto que podemos movilizarnos para evaluar las amenazas y disminuir nuestra
        vulnerabilidad.
        <br>
        Es por eso que en SIATA le apostamos a la educación ambiental, a través de procesos pedagógicos; Pequeños
        Científicos, grupos de estudiantes de comunidades educativas que trabaja temas de meteorología y experiencias
        científicas, actividades de apropiación del conocimiento del riesgo con Los Cuerpos Bomberos y los grupos
        Comités ambientales CUIDÁ del Área Metropolitana. Venimos desarrollando además un proceso de instalación y
        apropiación de unas alarmas comunitarias en diferentes comunidades en situación de vulnerabilidad del Valle de
        Aburrá, que permitirá a los habitantes de estas zonas a ser conscientes de la situación de riesgo y actuar a
        tiempo en caso de una emergencia.
        <br>
        En Colombia, tenemos una orientación dada por la Política Nacional de Educación Ambiental (PNEA) que nos convoca
        a vivir la educación como un proceso de aprendizaje que vive y apropia las relaciones entre la cultura, la
        sociedad y la naturaleza. Así, creemos en la educación como una oportunidad para que la ciudadanía reconozca y
        se reconozca desde las dinámicas cambiantes del territorio y desde su compromiso con la apropiación y uso de las
        herramientas y del conocimiento que genera la academia y las instituciones para la mitigación de riesgos
        latentes a los que estamos expuestos diariamente.
        <br>
    <h3>Un repaso por la historia</h3>
    <br>
    <b>1969</b> Se definió por primera vez el concepto de educación ambiental por William Stapp doctor de la
    Universidad de Míchigan.“La educación ambiental es una forma de concienciar a la población”
    <br>
    <b>1972</b> Declaración de la conferencia de las Naciones Unidas sobre el medio ambiente humano celebrada en
    Estocolmo (Suecia), dónde se expuso la necesidad de fortalecer los criterios para la conservación del lugar que
    habitamos. Se establece el Principio 19 que señala:“Es indispensable una educación en labores ambientales,
    dirigida tanto a las generaciones jóvenes como a los adultos... inspirada en el sentido de su responsabilidad en
    cuanto a la protección y mejoramiento del medio en toda su dimensión humana.”
    <br>
    <b>1975</b> Seminario internacional de educación ambiental de Belgrado (Serbia) se emitió la llamada Carta de
    Belgrado sobre educación ambiental, se le otorga a esta gran importancia en los procesos de cambio, se
    recomienda la enseñanza de nuevos conocimientos que sean la clave para conseguir el mejoramiento ambiental.
    Declaración del 26 de enero como el día mundial de la educación ambiental
    <br>
    <b>1977</b> Conferencia intergubernamental sobre educación ambiental celebrada en Tbilisi (URSS) se planteó la
    educación ambiental como una pedagogía de la acción para la acción, que consiste en hacer que cada persona
    comprenda al medio ambiente en su totalidad.
    <br>
    <b>1987</b> En moscú (URSS) surge la propuesta de una estrategia Internacional para la acción en el campo de la
    educación y formación ambiental para los años 1990 – 1999, se mencionan como las principales causas de la
    problemática ambiental a la pobreza, y al aumento de la población.
    <br>
    <b>1992</b> Cumbre de la Tierra, se emitieron varios documentos, entre los cuales es importante destacar la
    Agenda 21 la que contiene una serie de tareas: la reorientación de la educación hacia el desarrollo sostenible,
    el aumento de la conciencia del público, y el fomento a la capacitación.
    <br>
    <b>1992</b> Foro Global Ciudadano de Río 92 se aprobaron 33 tratados; uno de ellos lleva por título Educación
    Ambiental hacia Sociedades Sustentables y de Responsabilidad Global el cual parte de señalar a la educación
    ambiental como un acto para la transformación social, contempla a la educación como un proceso de aprendizaje
    permanente basado en el respeto a todas las formas de vida.
    <br>
    <b>1992</b> Primer Congreso Iberoamericano de Educación Ambiental en Guadalajara (México,) se estableció que la
    educación ambiental es eminentemente política y un instrumento esencial para alcanzar una sociedad sustentable y
    justa.
    <br>
    <b>1997</b> Conferencia Internacional Medio Ambiente y Sociedad en Sajonia (Grecia) establece que para alcanzar
    un nivel aceptable de sostenibilidad no sólo es necesario cuidar adecuadamente los recursos naturales, sino que
    implica solucionar otros aspectos que tienen íntima relación con el logro de una mejor calidad de vida en la
    población, por lo tanto sostiene que la noción de sostenibilidad incluye cuestiones no sólo de medio ambiente,
    sino también de pobreza, población, salud, seguridad alimentaria, democracia, derechos humanos y paz.
    <br>
    <b>1997</b> Segundo Congreso Iberoamericano de Educación Ambiental México bajo el lema Tras las huellas de
    Tbilisi.
    <br>
    <b>2000</b> Tercer Congreso Iberoamericano de Educación Ambiental, fue realizado en Caracas bajo el lema Pueblos
    y Caminos hacia el Desarrollo Sostenible.
    <br>
    <b>2003</b> Cuarto Congreso Iberoamericano de Educación Ambiental, realizado en Cuba bajo el lema Un mundo mejor
    es posible.
    <br>
    <b>2006</b> El Quinto Congreso Iberoamericano de educación ambiental se realizó en Brasil bajo el lema La
    contribución de la educación ambiental para la sustentabilidad planetaria
    <br>
    <b>2009</b> Sexto congreso Iberoamericano de educación ambiental se realizó en Argentina Enriqueciendo las
    propuestas educativo ambientales para la acción colectiva
    <br>
    <b>2014</b> Séptimo congreso Iberoamericano de educación ambiental se realizó en Perú educarnos juntos para la
    sustentabilidad de la vida y a la construcción de una ciudadanía ambiental informada y sensibilizada.

