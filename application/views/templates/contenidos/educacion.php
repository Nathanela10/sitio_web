    <h3>Pronóstico</h3>
    <p>
        ¿Cómo se hace un pronóstico del estado del tiempo? ¿De dónde obtenemos la información?
    </p>
    <p>
        En el SIATA realizamos el pronóstico del estado del tiempo basados en un modelo numérico meteorológico (WRF) que
        se ejecuta diariamente, el cual se complementa con información en tiempo real de la red de monitoreo
        hidrometeorológico instalada en el valle de Aburrá y sensores remotos.
    </p>
    <p>
        Para dar cuenta de la variabilidad e incertidumbre de las variables atmosféricas involucradas en el pronóstico,
        ejecutamos seis configuraciones diferentes del modelo que nos permiten hacer un análisis de la probabilidad de
        ocurrencia de precipitación. Es por esto que los pronósticos de lluvia los publicamos como probabilidad de baja,
        media o alta.
    </p>
    <p>
        Como las condiciones meteorológicas en el valle varían a lo largo del día, cada 6 horas realizamos una revisión
        de la información registrada por nuestros sensores remotos y de ser necesario un ajuste al pronóstico.
    </p>
    <p>
        La información del pronóstico se brinda para cada ventana de tiempo <br>
        Madrugada 00:00 a 06:00 <br>
        Mañana 06:00 a 12:00<br>
        Tarde 12:00 a 18:00<br>
        Noche 18:00 a 00:00
