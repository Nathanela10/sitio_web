    <h3>Sismos</h3>
    <p>
        Un sismo es una serie de vibraciones en la corteza terrestre originado por liberación de energía en la tierra
        ocasionado por rupturas o movimientos de falla, subducción de placas tectónicas y actividad magmática
        principalmente.
    </p>
    <p>
        La naturaleza de los sismos varían según el ambiente geodinámico en el que se encuentre una región, sobre
        Colombia convergen tres placas tectónicas (Nazca, Sudamérica y Caribe) y el bloque Panamá-Chocó, lo cual
        configura a nuestro país como una zona tectónicamente activa y compleja.
    </p>
    <img src="/sitio_web/application/assets/img/contenido/Educacion/imagen_sismos_1.png"
         style="width: 40vw; margin-left:15vw">
    <br/>
    <p>
        Colombia presenta un gran número de fallas en la zona de deformación originada por la actividad tectónica,
        conocida como los Andes del norte (consta de los sistemas montañosos de Ecuador y Colombia).
    </p>
    <p>
        Los sismos pueden ser superficiales (menor a 30 km) o profundos (mayor a 30 km), los primeros son asociados
        principalmente a fallas presente en la superficie terrestre, mientras que los profundos son asociados a zonas de
        subducción (rupturas de la placa en profundidad).
    </p>
    <p>
        Los sismos registrados en el valle de Aburrá en su mayoría están asociados a sistemas de fallas presente en el
        occidente y noroccidente del AMVA y a los nidos de Bucaramanga (% de los sismos vienen de esta zona) y viejo
        Caldas
    </p>
    <a></a>
    <p>
        Aunque no es posible realizar predicciones de cuándo ocurrirá un sismo, el monitoreo de estos permite establecer
        zonas donde existe mayor actividad sísmica; con lo que se modela la amenaza, vulnerabilidad y riesgo sísmico.
    </p>
    <img src="/sitio_web/application/assets/img/contenido/Educacion/imagen_sismos_1.jpg"
         style="width: 40vw; margin-left:15vw">
    <br/>
    <p>
        Actualmente el SIATA administra el Red Acelerográfica de Medellín y el Valle de Aburrá, la cual se compone de 26
        estaciones activas con las cuales es monitoreada la actividad sísmica en el región. Los registros
        acelerográficos permiten medir, como su nombre lo indica, la aceleración que se registró en el suelo ante un
        sismo y esto está asociado a la intensidad del evento.
    </p>
    <p>
        Los estudios de la aceleración del suelo o roca son usados para establecer normas sísmicas, para Colombia se ha
        realizado la norma sismo resistente del 2010 (NSR­10). También es importante destacar la importancia que tiene
        una red de acelerógrafos al momento de ejecutar proyectos de microzonificación sísmicas (obligatorio para
        capitales departamentales y ciudades con más de 100,000 habitantes que se encuentren en zona de amenaza
        intermedia alta)
    </p>
    <p>
        Aunque en el valle de Aburrá no haya registro de una gran afectación por parte de un evento sísmico (La Red
        Sismológica Nacional de Colombia se empezó a construir en 1993) Vale la pena recordar que nos encontramos en una
        zona compleja y activa desde el punto de vista geodinámico y que la actividad humana es muy pequeña en
        comparación a procesos geológicos recientes, que configura la geología actual del valle de Aburrá.
    </p>
